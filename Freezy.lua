			TextBox.RemoteHighlight_.Text = Highlight(s, {"FireServer", "fireServer", "InvokeServer", "invokeServer", "/respawn", "/R6"})
			TextBox.Tokens_.Text = hTokens(s)
			TextBox.Numbers_.Text = numbers(s)
			TextBox.Strings_.Text = strings(s)
			local lin = 1
			s:gsub("\n", function()
				lin = lin + 1
			end)
			Lines.Text = ""
			for i = 1, lin do
				Lines.Text = Lines.Text .. i .. "\n"
			end
		end
	end
	
	highlight_source("Text")
	
	TextBox.Changed:Connect(highlight_source)
	
	
